#include "holberton.h"

/**
 * print_sign - compare if c is lowercase or no
 * @n:int checked
 * Return: 0 on success
 */

int print_sign(int n)
{
	if (n > 0)
	{
		_putchar('+');
		return (1);
	}
	if (n == 0)
	{
		_putchar('0');
		return (0);
	}
	if (n < 0)
	{
		_putchar('-');
		return (-1);
	}
	return (0);
}
